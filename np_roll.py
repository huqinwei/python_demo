import numpy as np
x = np.arange(12)
print(x)

x2 = np.reshape(x,(3,4))
print(x2)

x3 = np.roll(x2,1)
print("np.roll(x2,1)")
print(x3)
x4 = np.roll(x2,1,axis=0)
print("np.roll(x2,1,axis=0)")
print(x4)
x42 = np.roll(x2,1,0)
print("np.roll(x2,1,0)")
print(x42)
x5 = np.roll(x2,1,axis=1)
print("np.roll(x2,1,axis=1)")
print(x5)

x6 = np.roll(x2,-1)
print("np.roll(x2,-1)")
print(x6)

x7 = np.roll(x2,(1,1),axis=(0,1))
print("np.roll(x2,(1,1),axis=(0,1))")
print(x7)
x72 = np.roll(x2,(1,1),axis=(1,0))
print("np.roll(x2,(1,1),axis=(1,0))")
print(x72)
x8 = np.roll(x2,(1,1),axis=(0,0))
print("np.roll(x2,(1,1),axis(0,0))")
print(x8)
x9 = np.roll(x2,(1,1),axis=(1,1))
print("np.roll(x2,(1,1),axis=(1,1))")
print(x9)
